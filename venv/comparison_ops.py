# Comparison operaters and conditional (control structures: if, elif, else)
#Control Structures: if, elif, else
#If, else/if and else — if, elif and else — are control structures in Python.

name = input("Enter your name: ")

if name == "Shamayla":
    print(f"Hello {name}! Good to see you")
elif name == "Aminah":
    print(f"Hey {name}, What's your goal of the day?")
else:
    print("Have a nice day!")
